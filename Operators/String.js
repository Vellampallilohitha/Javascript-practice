const str1 = "Hello";
const str2 = "World";

console.log("str1 + str2 →", str1 + str2);
console.log("str1 + ' ' + str2 →", str1 + ' ' + str2);
console.log("str1.length →", str1.length);
console.log("str2.length →", str2.length);
console.log("str1 === str2 →", str1 === str2);
console.log("str1 !== str2 →", str1 !== str2);